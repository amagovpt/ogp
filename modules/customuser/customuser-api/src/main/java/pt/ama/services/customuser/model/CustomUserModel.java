package pt.ama.services.customuser.model;

public class CustomUserModel {

	private String email;
	private String fullName;
	private String identificationId;
	private String lawerId;
	private String solicitorId;
	private String notaryId;
	private String passportId;

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getFullName() {
		return fullName;
	}

	public void setFullName(String fullName) {
		this.fullName = fullName;
	}

	public String getIdentificationId() {
		return identificationId;
	}

	public void setIdentificationId(String identificationId) {
		this.identificationId = identificationId;
	}

	public String getLawerId() {
		return lawerId;
	}

	public void setLawerId(String lawerId) {
		this.lawerId = lawerId;
	}

	public String getSolicitorId() {
		return solicitorId;
	}

	public void setSolicitorId(String solicitorId) {
		this.solicitorId = solicitorId;
	}

	public String getNotaryId() {
		return notaryId;
	}

	public void setNotaryId(String notaryId) {
		this.notaryId = notaryId;
	}

	public String getPassportId() {
		return passportId;
	}

	public void setPassportId(String passportId) {
		this.passportId = passportId;
	}
	
}
